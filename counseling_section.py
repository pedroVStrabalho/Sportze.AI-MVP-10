def choose_generic_individual_tournament(sport: str, goal: str, level: str, location: str) -> Dict[str, str]:
    canonical, _, _ = normalize_sport_name(sport)
    options = GENERIC_INDIVIDUAL_TOURNAMENT_CATALOG.get(canonical, [])
    if not options:
        return {"name": f"{canonical or sport} National Open / Federation Ranking Event", "note": "custom-sport fallback until live API search is connected", "reason": "For sports outside the current catalog, the safest named recommendation is the next official federation ranking event in the athlete's current region."}
    goal_l = normalize_text_for_match(goal)
    level_l = normalize_text_for_match(level)
    if "bigger" in goal_l or "international" in goal_l or "professional" in level_l or level_l == "yes":
        chosen = options[min(1, len(options) - 1)]
    elif "match" in goal_l or "wins" in goal_l or "confidence" in goal_l or "low" in goal_l:
        chosen = options[-1]
    else:
        chosen = options[0]
    return {"name": chosen["name"], "note": chosen["note"], "reason": f"Selected for {canonical or sport} based on goal='{goal or 'not provided'}', level='{level or 'not provided'}', and location='{location or 'not provided'}'."}

